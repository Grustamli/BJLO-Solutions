package introduction;

public class P1_12 {
    public static void main(String[] args) {
        System.out.println("I'm Nobody! Who are you?");
        System.out.println("Are you -- Nobody -- Too?");
        System.out.println("Then there's a pair of us!");
        System.out.println("Don't tell! they'd advertise -- you know!");
        System.out.println();
        System.out.println("How dreary -- to be -- Somebody!");
        System.out.println("How public -- like a Frog --");
        System.out.println("To tell one's name -- the livelong June --");
        System.out.println("To an admiring Bog!");
    }
}
